// named function
function perkalian (a,b){
    let result = a*b
    return result
}

// expresif function
// arrow function
// anonymous function
const penambahan = (a,b) => {
    let result = a+b
    return result
}

// named expression function
const pembagian = function (a,b) {
    let result = a/b
    return result
}

console.log(perkalian(5,2))
console.log(penambahan(6,2))
console.log(pembagian(8,2))
